func (g *AwsClient) GetSecret(ctx context.Context, name string) ([]byte, error) {
	l := log.WithFields(log.Fields{
		"action": "GetSecret",
	})
	l.Trace("start")
	defer l.Trace("end")
	g.arnMu.RLock()
	arn := g.accountSecretArns[name]
	g.arnMu.RUnlock()
	
	// Ensure circuit breaker is initialized
	g.ensureBreaker()
	
	// Wrap AWS API call with circuit breaker
	resp, err := circuitbreaker.ExecuteTyped(g.breaker, ctx, func(ctx context.Context) (*secretsmanager.GetSecretValueOutput, error) {
		return g.client.GetSecretValue(ctx, &secretsmanager.GetSecretValueInput{
			SecretId: &arn,
		})
	})
	if err != nil {
		l.WithError(err).Error("Failed to get secret value")
		return nil, circuitbreaker.WrapError(err, g.breaker.Name(), g.breaker.State())
	}
	return []byte(*resp.SecretString), nil
}

func (c *AwsClient) createSecret(ctx context.Context, name string, secret []byte) error {
	l := log.WithFields(log.Fields{
		"action": "createSecret",
		"name":   name,
		"driver": c.Driver(),
	})
	l.Trace("start")
	defer l.Trace("end")
	csi := &secretsmanager.CreateSecretInput{
		Name:         &name,
		Description:  aws.String("managed by SecretSync. do not edit directly."),
		SecretString: aws.String(string(secret)),
	}
	if c.EncryptionKey != "" {
		csi.KmsKeyId = aws.String(c.EncryptionKey)
	}
	if len(c.ReplicaRegions) > 0 {
		var rep []types.ReplicaRegionType
		for _, r := range c.ReplicaRegions {
			rr := types.ReplicaRegionType{
				Region: aws.String(r),
			}
			if c.EncryptionKey != "" {
				rr.KmsKeyId = aws.String(c.EncryptionKey)
			}
			rep = append(rep, rr)
		}
		csi.AddReplicaRegions = rep
	}
	if c.Tags != nil {
		var tags []types.Tag
		for k, v := range c.Tags {
			tags = append(tags, types.Tag{
				Key:   aws.String(k),
				Value: aws.String(v),
			})
		}
		csi.Tags = tags
	}
	
	// Ensure circuit breaker is initialized
	c.ensureBreaker()
	
	// Wrap AWS API call with circuit breaker
	_, err := circuitbreaker.ExecuteTyped(c.breaker, ctx, func(ctx context.Context) (*secretsmanager.CreateSecretOutput, error) {
		return c.client.CreateSecret(ctx, csi)
	})
	if err != nil {
		l.WithError(err).Error("Failed to create secret")
		return circuitbreaker.WrapError(err, c.breaker.Name(), c.breaker.State())
	}
	return nil
}

