func NewClient(cfg *AwsClient) (*AwsClient, error) {
	l := log.WithFields(log.Fields{
		"action": "NewClient",
	})
	l.Trace("start")
	vc := &AwsClient{}
	jd, err := json.Marshal(cfg)
	if err != nil {
		l.Debugf("error: %v", err)
		return nil, err
	}
	err = json.Unmarshal(jd, &vc)
	if err != nil {
		l.Debugf("error: %v", err)
		return nil, err
	}
	if vc.Region == "" {
		vc.Region = "us-east-1"
	}
	// Set default cache TTL if not configured
	if vc.CacheTTL == 0 {
		vc.CacheTTL = 5 * time.Minute
	}
	
	// Initialize circuit breaker for AWS API calls
	breakerName := fmt.Sprintf("aws-secretsmanager-%s-%s", vc.Name, vc.Region)
	vc.breaker = circuitbreaker.New(circuitbreaker.DefaultConfig(breakerName))
	
	l.Debugf("client=%+v", vc)
	l.Trace("end")
	return vc, nil
}

// ensureBreaker initializes circuit breaker if not already initialized
// This handles cases where AwsClient is created without NewClient (e.g., in tests)
// Uses sync.Once to ensure thread-safe initialization
func (g *AwsClient) ensureBreaker() {
	g.breakerOnce.Do(func() {
		if g.breaker == nil {
			breakerName := fmt.Sprintf("aws-secretsmanager-%s-%s", g.Name, g.Region)
			if breakerName == "aws-secretsmanager--" {
				breakerName = "aws-secretsmanager-default"
			}
			g.breaker = circuitbreaker.New(circuitbreaker.DefaultConfig(breakerName))
		}
	})
}

func (c *AwsClient) CreateClient(ctx context.Context) error {
	return c.CreateClientWithEndpoint(ctx, "")
}

// CreateClientWithEndpoint creates a client with an optional custom endpoint (for LocalStack)
func (c *AwsClient) CreateClientWithEndpoint(ctx context.Context, endpoint string) error {
	l := log.WithFields(log.Fields{
		"action":   "CreateClient",
		"endpoint": endpoint,
	})
	l.Trace("start")

	// Configure HTTP client with timeout to prevent hung connections
	httpClient := &http.Client{
		Timeout: 30 * time.Second,
	}

	awscfg, err := config.LoadDefaultConfig(ctx, config.WithHTTPClient(httpClient))
	if err != nil {
		l.Debugf("error: %v", err)
		return err
	}
	var provider aws.CredentialsProvider
	if c.RoleArn != "" {
		stsclient := sts.NewFromConfig(awscfg)
		provider = stscreds.NewAssumeRoleProvider(stsclient, c.RoleArn)
		awscfg.Credentials = provider
	}

	opts := secretsmanager.Options{
		Region:      c.Region,
		Credentials: awscfg.Credentials,
	}

	// Support custom endpoint for LocalStack/testing
	if endpoint != "" {
		opts.BaseEndpoint = aws.String(endpoint)
	}

	svc := secretsmanager.New(opts)
	c.client = svc
	l.Trace("end")
	return nil
}

