// ensureBreaker initializes circuit breaker if not already initialized
// This handles cases where VaultClient is created without NewClient (e.g., in tests)
// Uses sync.Once to ensure thread-safe initialization
func (vc *VaultClient) ensureBreaker() {
	vc.breakerOnce.Do(func() {
		if vc.breaker == nil {
			breakerName := fmt.Sprintf("vault-%s", vc.Address)
			if breakerName == "vault-" {
				breakerName = "vault-default"
			}
			vc.breaker = circuitbreaker.New(circuitbreaker.DefaultConfig(breakerName))
		}
	})
}

// listPathContents performs the actual Vault LIST operation with circuit breaker
func (vc *VaultClient) listPathContents(ctx context.Context, metadataPath string) ([]string, error) {
	logical := vc.getLogicalClient()
	if logical == nil {
		return nil, errors.New("vault client not initialized")
	}
	
	// Ensure circuit breaker is initialized
	vc.ensureBreaker()
	
	// Wrap Vault API call with circuit breaker
	result, err := circuitbreaker.ExecuteTyped(vc.breaker, ctx, func(ctx context.Context) (*api.Secret, error) {
		return logical.ListWithContext(ctx, metadataPath)
	})
	if err != nil {
		return nil, circuitbreaker.WrapError(err, vc.breaker.Name(), vc.breaker.State())
	}
	
	secret := result
	if secret == nil {
		return nil, nil
	}
	if secret.Data == nil || secret.Data["keys"] == nil {
		return nil, nil
	}
	
	// Type-safe extraction of keys
	keysInterface, ok := secret.Data["keys"].([]interface{})
	if !ok {
		return nil, fmt.Errorf("unexpected keys type in Vault response: %T", secret.Data["keys"])
	}
	
	var keys []string
	for i, v := range keysInterface {
		keyStr, ok := v.(string)
		if !ok {
			return nil, fmt.Errorf("unexpected key type at index %d: %T", i, v)
		}
		keys = append(keys, keyStr)
	}
	return keys, nil
}

func (vc *VaultClient) ListSecrets(ctx context.Context, p string) ([]string, error) {
	var keys []string
	var err error
	terr := vc.NewToken(ctx)
	if terr != nil {
		return keys, terr
	}
	keys, err = vc.ListSecretsOnce(ctx, p)
	if err != nil {
		terr := vc.NewToken(ctx)
		if terr != nil {
			return keys, terr
		}
		keys, err = vc.ListSecretsOnce(ctx, p)
		if err != nil {
			return keys, err
		}
	}
	return keys, err
}

func (c *VaultClient) Close() error {
	c.Client.ClearToken()
	return nil
}

func (c *VaultClient) SetDefaults(defaults any) error {
	dv, err := json.Marshal(defaults)
	if err != nil {
		return err
	}
	dc := &VaultClient{}
	err = json.Unmarshal(dv, dc)
	if err != nil {
		return err
	}
	if c.Address == "" && dc.Address != "" {
		c.Address = dc.Address
	}
	if c.CIDR == "" && dc.CIDR != "" {
		c.CIDR = dc.CIDR
	}
	if c.AuthMethod == "" && dc.AuthMethod != "" {
		c.AuthMethod = dc.AuthMethod
	}
	if c.Namespace == "" && dc.Namespace != "" {
		c.Namespace = dc.Namespace
	}
	if c.Role == "" && dc.Role != "" {
		c.Role = dc.Role
	}
	if c.TTL == "" && dc.TTL != "" {
		c.TTL = dc.TTL
	}
	return nil
}
